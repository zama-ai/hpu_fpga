// Local Ack -> IOp lookup
#define ACK_IOP_DEPTH 256



// Type
// ============================================================================================= //
// Lookup entry
typedef struct {
  volatile uint32_t* ptr;
  size_t len;
} Lookup_t;

// Local Ack -> IOp lookup
// Use to store match between received ack and corresponding IOp
typedef struct {
  uint32_t iop[ACK_IOP_DEPTH];
  size_t wr_idx;
  size_t rd_idx;
} AckIopLut_t;

